

function findMax(arr){
    let max = arr[0];
    for(let i=0; i< arr.length; i++){
        if(arr[i] > max){
            max = arr[i];
        }
       
    }
    return max;
}

let arr = [34,53,65,332,543,2,3,5,6]; 
console.log(findMax(arr)); // 543



