

function findMin(arr){
    let min = arr[0];
    for(let i=0; i< arr.length; i++){
        if(arr[i] < min){
            min = arr[i];
        }
       
    }
    return min;
}

let arr = [34,53,65,332,543,2,3,5,6]; 
console.log(findMin(arr)); // 2


